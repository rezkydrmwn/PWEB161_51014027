<h1><p>PERATURAN</p>
<p>LAPANGAN</p>
<ul>
<li>Ukuran: panjang 25-42 m x lebar 15-25 m</li>
<li>Garis batas: garis selebar 8 cm, yakni garis sentuh di sisi, garis gawang di 	 ujung-ujung, dan garis melintang tengah lapangan; 3 m lingkaran tengah; tak 	 ada tembok penghalang atau papan</li>
<li>Daerah penalti: busur berukuran 6 m dari setiap pos</li>
<li>Garis penalti: 6 m dari titik tengah garis gawang</li>
<li>Garis penalti kedua: 12 m dari titik tengah garis gawang</li>
<li>Zona pergantian: daerah 6 m (3 m pada setiap sisi garis tengah lapangan) pada 	sisi tribun dari pelemparan</li>
<li>Gawang: tinggi 2 m x lebar 3 m</li>
<li>Permukaan daerah pelemparan: halus, rata, dan tak abrasif</li></ul>
</h1>