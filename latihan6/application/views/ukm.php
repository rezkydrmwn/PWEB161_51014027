UKM FUTSAL

VISI :
<ul>
<li>Membentuk anak-anak muda yang berkarakter,bermental kuat,<br>berjiwa sportif dalam suasana kebersamaan dalam rangka meraih prestasi tinggi.<br>Menjadikan olahraga futsal sebagai pelayanan olahraga yang membawa dampak buat banyak orang.</li>
</ul>

MISI :
<ul> <li>Adanya saling mendukung di pelayanan futsal.
<li>Menjadikan UKM Futsal Kharisma sebagai keluarga.
<li>Menjalin persaudaran yang kokoh di antara anggota UKM. 
<li>Menjadikan UKM Futsal sebagai hobi yang favorit. </ul> </p> 

